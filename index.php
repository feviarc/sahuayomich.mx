										En el segundo plano al fondo, resaltan a la vista tres colinas, que significan según la tradición,
										los tres pueblos que dieron origen a Sahuayo, en la segunda mitad del siglo XVI, también según
										la intención del diseñador se pueden considerar los tres gobiernos: federal, estatal y municipal en
										franca y decidida coordinación.
									</p>
									<p>
										En el tercer campo se observa una serpiente emplumada o dios Quetzalcoatl de los mexicas luchando con
