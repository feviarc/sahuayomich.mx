									<h2>Record Guinness: El huarache más grande el mundo.</h2>
									<p>
										Sahuayo es una ciudad con vocación de comercio pero también es conocida y visitada por su oferta y variedad
										de huaraches producidos. La cadena productiva del huarachero de Sahuayo se ve enriquecida con la presencia de materias
										primas básicas que van desde la piel, la curtiduría, y demás proveeduría que requiere esta actividad artesanal y que
										cada vez gana más adeptos no sólo en el país, sino en el extranjero sobre todo en California, Estados Unidos, donde
										el mercado de la nostalgia recibe y demanda cada vez más este icónico producto.
									</p>
									<p>
										Se sabe que la producción de huaraches en Sahuayo data desde hace aproximadamente 100 años. Y es que el aprendizaje de
										esta labor artesanal, se va heredando de generación en generación. Sin embargo, esta tradición ha ido disminuyendo debido
										al proceso de industrialización de la actividad. Es por eso que, para apoyar la labor de esta actividad y posicionar al
										huarachero michoacano en la lista de exportaciones en franco crecimiento, la Coordinación de Economía del Ayuntamiento de
										Sahuayo convocó a fabricar el huarache más grande del mundo.
									</p>
									<p>
										Sahuayo, Michoacán es conocido por sus artesanos de huaraches, que son sandalias tradicionales de los campesinos
										mexicanos. El día 24 de noviembre de 2016, nuestra ciudad fue reconocida como escenario de un nuevo récord mundial.
										Tras días de labor, artesanos patrocinados por el municipio de Sahuayo elaboraron un huarache con una longitud
										de 7,45 m y un ancho de 3,09 m, logrando batir el récord por la sandalia más grande del mundo. El grupo de más de 30
										artesanos utilizó alrededor de 80 metros cuadrados de cuero para fabricar el huarache, que fue desvelado en la ceremonia
										de arranque de la Expo Feria de Sahuayo 2016.
									</p>
