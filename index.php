								<div class="col-md-6">
									<h2>San José Sánchez del Río.</h2>
									<p>
										José Sánchez del Río nació el 28 de marzo de 1913 en Sahuayo, Michoacán, en una familia profundamente
										religiosa encabezada por Macario Sánchez y María del Río. Desde niño mostró un carácter amable y
										decidido, con un gran amor por Dios inculcado por su familia. Durante la Guerra Cristera, que
										surgió en respuesta a las leyes anticlericales del gobierno mexicano, José, con solo 13 años,
										pidió unirse a los Cristeros. Aunque inicialmente fue rechazado por su edad, su fervor religioso
										lo llevó a ser aceptado como asistente y abanderado del grupo.
									</p>
									<p>
										En febrero de 1928, durante una batalla, José ofreció su caballo al líder Cristero cuando el suyo
										fue abatido, lo que resultó en su captura por el ejército federal. Fue encarcelado y sometido a
										torturas para que renunciara a su fe, pero José permaneció firme. Durante su encarcelamiento,
										escribió cartas a su familia expresando su amor a Dios y aceptando con serenidad su destino.
									</p>
									<p>
										El 10 de febrero de 1928, tras días de tortura, José fue obligado a caminar descalzo por las calles
										de Sahuayo hacia su ejecución. Los soldados lo golpearon e hicieron heridas en sus pies, pero él
										repetía con valentía: "¡Viva Cristo Rey! ¡Viva la Virgen de Guadalupe!". Finalmente, fue asesinado
										en el panteón municipal de Sahuayo, entregando su vida por su fe con solo 14 años.
									</p>
									<p>
										La santidad de José fue rápidamente reconocida por la comunidad. En 1996, el Papa Juan Pablo II
										lo declaró "Siervo de Dios", y en 2005, el Papa Benedicto XVI lo beatificó como mártir de la fe.
										El 16 de octubre de 2016, el Papa Francisco lo canonizó en una ceremonia en el Vaticano, reconociéndolo
										como ejemplo de fe inquebrantable.
									</p>
